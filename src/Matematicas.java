/**
 * Clase que contiene métodos matemáticos
 */
public class Matematicas {
    /**
     * Método que suma dos números
     * @param num1 Número 1
     * @param num2 Número 2
     * @return Suma de los dos números
     */
    public static int suma(int num1, int num2){
        return num1 + num2;
    }
}
